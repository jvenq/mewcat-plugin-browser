#!/usr/bin/env node

const fs = require("fs")
const path = require("path")
const crypto = require("crypto")

const INPUT_PATH = path.resolve(
    __dirname,
    "../assets/request_modifier_rule.json"
)
const OUTPUT_PATH = path.resolve(
    __dirname,
    "../src/background/config/hotlink-sites.generated.ts"
)
const CHECK_MODE = process.argv.includes("--check")

function escapeRegex(value) {
    return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")
}

function extractReferer(rule) {
    const headers = rule?.action?.requestHeaders
    if (!Array.isArray(headers)) {
        return null
    }
    const refererHeader = headers.find(
        header =>
            String(header?.header || "").toLowerCase() === "referer" &&
            String(header?.operation || "").toLowerCase() === "set" &&
            typeof header?.value === "string"
    )
    return refererHeader?.value || null
}

function extractOrigin(rule) {
    const headers = rule?.action?.requestHeaders
    if (!Array.isArray(headers)) {
        return undefined
    }
    const originHeader = headers.find(
        header =>
            String(header?.header || "").toLowerCase() === "origin" &&
            String(header?.operation || "").toLowerCase() === "set" &&
            typeof header?.value === "string"
    )
    return originHeader?.value || undefined
}

function extractHostToken(urlFilter) {
    if (typeof urlFilter !== "string") {
        return null
    }
    const match = urlFilter.match(/^https?:\/\/([^/]+)(?:\/.*)?$/i)
    return match?.[1] || null
}

function hasUnsupportedHostSyntax(hostToken) {
    // We intentionally skip complex regex-like host tokens.
    return /[()|[\]{}]/.test(hostToken)
}

function hostTokenToPattern(hostToken) {
    const escaped = hostToken
        .split("*")
        .map(part => escapeRegex(part))
        .join(".*")
    return `^${escaped}$`
}

function buildRuleOutput(rule) {
    const referer = extractReferer(rule)
    if (!referer) {
        return {
            output: null,
            skipReason: "missing referer header"
        }
    }

    const hostToken = extractHostToken(rule?.condition?.urlFilter)
    if (!hostToken) {
        return {
            output: null,
            skipReason: "invalid urlFilter format"
        }
    }

    if (hasUnsupportedHostSyntax(hostToken)) {
        return {
            output: null,
            skipReason: `unsupported host token syntax: ${hostToken}`
        }
    }

    return {
        output: {
            ruleKey: `immersive-${rule.id}`,
            matchHostPattern: hostTokenToPattern(hostToken),
            referer,
            origin: extractOrigin(rule),
            priority: 100 + Number(rule?.priority || 0)
        },
        skipReason: null
    }
}

function serializeGeneratedFile(generatedRules, meta) {
    const lines = []
    lines.push("// This file is auto-generated by scripts/sync-hotlink-sites-from-immersive.js")
    lines.push("// Do not edit manually.")
    lines.push("")
    lines.push("/* cspell:disable */")
    lines.push("")
    lines.push("export interface GeneratedHotlinkSiteRule {")
    lines.push("    ruleKey: string")
    lines.push("    matchHostPattern: string")
    lines.push("    referer: string")
    lines.push("    origin?: string")
    lines.push("    priority: number")
    lines.push("}")
    lines.push("")
    lines.push("export const GENERATED_HOTLINK_SITE_RULES: GeneratedHotlinkSiteRule[] = [")

    generatedRules.forEach(rule => {
        lines.push("    {")
        lines.push(`        ruleKey: ${JSON.stringify(rule.ruleKey)},`)
        lines.push(
            `        matchHostPattern: ${JSON.stringify(rule.matchHostPattern)},`
        )
        lines.push(`        referer: ${JSON.stringify(rule.referer)},`)
        if (rule.origin) {
            lines.push(`        origin: ${JSON.stringify(rule.origin)},`)
        }
        lines.push(`        priority: ${rule.priority}`)
        lines.push("    },")
    })

    lines.push("]")
    lines.push("")
    lines.push("export const GENERATED_HOTLINK_SITE_RULES_META = {")
    lines.push(`    sourcePath: ${JSON.stringify(meta.sourcePath)},`)
    lines.push(`    sourceHash: ${JSON.stringify(meta.sourceHash)},`)
    lines.push(`    totalInputRules: ${meta.totalInputRules},`)
    lines.push(`    totalOutputRules: ${meta.totalOutputRules},`)
    lines.push(`    skippedRules: ${meta.skippedRules}`)
    lines.push("}")
    lines.push("")
    lines.push("/* cspell:enable */")
    lines.push("")

    return lines.join("\n")
}

function main() {
    const sourceRaw = fs.readFileSync(INPUT_PATH, "utf8")
    const sourceRules = JSON.parse(sourceRaw)
    if (!Array.isArray(sourceRules)) {
        throw new Error("request_modifier_rule.json must be an array")
    }
    const sourceHash = crypto
        .createHash("sha256")
        .update(sourceRaw)
        .digest("hex")

    const generatedRules = []
    const skipped = []

    sourceRules.forEach(rule => {
        const { output, skipReason } = buildRuleOutput(rule)
        if (!output) {
            skipped.push({ id: rule?.id, reason: skipReason })
            return
        }
        generatedRules.push(output)
    })

    generatedRules.sort((a, b) => a.ruleKey.localeCompare(b.ruleKey))

    const fileContent = serializeGeneratedFile(generatedRules, {
        sourcePath: path.relative(path.resolve(__dirname, ".."), INPUT_PATH),
        sourceHash,
        totalInputRules: sourceRules.length,
        totalOutputRules: generatedRules.length,
        skippedRules: skipped.length
    })

    if (CHECK_MODE) {
        const current = fs.existsSync(OUTPUT_PATH)
            ? fs.readFileSync(OUTPUT_PATH, "utf8")
            : ""
        if (current !== fileContent) {
            console.error(
                "[sync-hotlink-sites] generated file is out of date:",
                path.relative(path.resolve(__dirname, ".."), OUTPUT_PATH)
            )
            skipped.forEach(item => {
                console.error(
                    `  - skipped id=${String(item.id)} reason=${item.reason}`
                )
            })
            process.exit(1)
        }
        console.log("[sync-hotlink-sites] generated file is up to date")
        return
    }

    fs.writeFileSync(OUTPUT_PATH, fileContent, "utf8")

    console.log(
        "[sync-hotlink-sites] wrote",
        path.relative(path.resolve(__dirname, ".."), OUTPUT_PATH)
    )
    console.log(
        `[sync-hotlink-sites] rules: input=${sourceRules.length}, output=${generatedRules.length}, skipped=${skipped.length}`
    )
    skipped.forEach(item => {
        console.warn(
            `  - skipped id=${String(item.id)} reason=${item.reason}`
        )
    })
}

main()
